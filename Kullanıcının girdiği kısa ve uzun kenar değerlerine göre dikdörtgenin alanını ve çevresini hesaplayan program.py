# Kullanıcının girdiği kısa ve uzun kenar değerlerine göre dikdörtgenin alanını ve çevresini hesaplayınız.

kisaKenar = int(input('Kısa kenar giriniz: '))
uzunKenar = int(input('Uzun kenar giriniz: '))

alanHesap = (kisaKenar * uzunKenar)
cevreHesap = ((kisaKenar + uzunKenar)*2)

print(f'\nDiktörgenin Alanı: {alanHesap}\nDikdörtgenin Çevresi: {cevrehesap}')
